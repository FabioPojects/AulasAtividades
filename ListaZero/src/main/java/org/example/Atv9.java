package org.example;

public class Atv9 {
    public static void main(String[] args) {
        System.out.println("Quadrado dos números de 0 até 10");

        for (int i = 0; i <= 10 ; i++) {
            int quadrado = i * i;
            System.out.println("O quadrado de " + i + " é " + quadrado);
        }
    }
}
